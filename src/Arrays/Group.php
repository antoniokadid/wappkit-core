<?php

namespace AntonioKadid\WAPPKitCore\Arrays;

class Group
{
     /** @var array */
     private $array;

     /** @var array */
     private $keySelectors = [];

    /**
     * @param array $array
     */
    public function __construct(array &$array)
    {
        $this->array = &$array;
    }

    /**
     * @suppressWarnings(PHPMD.ShortMethodName)
     */
    public function go(): void
    {
        self::group($this->array, $this->keySelectors);
    }

    /**
     * @param callable $keySelector
     *
     * @return Group
     */
    public function key(callable $keySelector): Group
    {
        array_push($this->keySelectors, $keySelector);

        return $this;
    }

    /**
     * Group $array based on the keys that are generated by $selectors.
     *
     * @param array      $array
     * @param callable[] $selectors
     *
     * @return array
     */
    private static function group(array &$array, callable ...$selectors): void
    {
        $currentSelector = array_shift($selectors);
        if ($currentSelector === null) {
            return;
        }

        $result = [];
        foreach ($array as $item) {
            $offset = call_user_func($currentSelector, $item);

            if (!isset($result[$offset])) {
                $result[$offset] = [];
            }

            array_push($result[$offset], $item);
        }

        if (!empty($selectors)) {
            array_walk(
                $result,
                function (&$value) use ($selectors) {
                    call_user_func([Utils::class, 'group'], $value, $selectors);
                }
            );
        }

        $array = &$result;
    }
}
